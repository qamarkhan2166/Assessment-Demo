    reviews_text_count = reviewsTextCount ?: 0,
    saturated_color = saturatedColor ?: "",
    slug = slug ?: "",
    suggestions_count = suggestionsCount ?: 0,
    tba = tba ?: false,
    updated = updated ?: ""
